// Hilfsfunktion: Rituale nach Monat gruppieren
export function getRitualeNachMonat(): Map<number, Ritual[]> {
  const map = new Map<number, Ritual[]>();
  for (const r of RITUALE_2026) {
    const list = map.get(r.monat) || [];
    list.push(r);
    map.set(r.monat, list);
  }
  return map;
}

